def secant_method(f, x0, x1, tol=1e-6, max_iter=100):
    """
    Реализация метода секущих для нахождения корня уравнения f(x)=0.

    :param f: Функция, корень которой мы ищем.
    :param x0: Первое начальное приближение.
    :param x1: Второе начальное приближение.
    :param tol: Допустимая погрешность.
    :param max_iter: Максимальное число итераций.
    :return: Приближенное значение корня уравнения.
    """
    iter_count = 0
    while abs(f(x1)) > tol and iter_count < max_iter:
        # Вычисляем следующее приближение с использованием хорды
        x_next = x1 - f(x1) * (x1 - x0) / (f(x1) - f(x0))
        # Обновляем значения
        x0 = x1
        x1 = x_next
        iter_count += 1
    return x1

# Пример использования:
def f(x):
    return x**2 - 4

# Начальные приближения
x0 = 1
x1 = 3

# Вызов метода секущих
root = secant_method(f, x0, x1)
print("Приближенный корень:", root)
